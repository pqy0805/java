package com.pqy.pojo;

public class Class {
	int classNum;
	String prof;
	public Class() {}
	public Class(int classNum, String prof) {
		super();
		this.classNum = classNum;
		this.prof = prof;
	}
	public int getClassNum() {
		return classNum;
	}
	public void setClassNum(int classNum) {
		this.classNum = classNum;
	}
	public String getProf() {
		return prof;
	}
	public void setProf(String prof) {
		this.prof = prof;
	}
	

}
